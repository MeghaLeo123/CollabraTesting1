class Switch
{
public static void main(String[] args)
{
char input='y';
switch(input)
{
case 'y':System.out.println("My initial is :" +input);
         break;
case 'z':System.out.println("My initial is :" +input);
         break;
case 'a':System.out.println("My initial is :" +input);
         break;
}
}
}